
public class Matematica {
	public static int somar (int a, int b) { /* quando vc define um método como Static 
												voce esta dizendo que ele pertence a classe isso
												significa que vc não precisa mais de um objeto pra
												chamar esse método, não é mais preciso chamar a instancia
												do objeto na classe main */
		return a + b;
	}
	
	public static int subtrair (int a, int b) {
		return a - b;
	}
}
